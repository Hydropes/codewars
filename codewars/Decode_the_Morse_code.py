# print(MORSE_CODE)


import re
def decode_morse(morse_code:str):
    morse_code=morse_code.upper().strip()
    while re.findall(r'\s{3}', morse_code):
        morse_code= re.sub(r'\s{3}', '  ', morse_code)
    return ''.join([MORSE_CODE.get(m, ' ') for m in morse_code.split(' ')])

print(decode_morse('...  ---  ...'))
print(decode_morse('.     .'))
